package cn.edu.zucc.ordercontrol.model;

public class StockOfMaterial {
	private String StockOfMaterialId;
	private String MaterialId;
	private String StockOfMaterialCount;
	private String StockOfMaterialAddress;
	public String getStockOfMaterialId() {
		return StockOfMaterialId;
	}
	public void setStockOfMaterialId(String stockOfMaterialId) {
		StockOfMaterialId = stockOfMaterialId;
	}
	public String getMaterialId() {
		return MaterialId;
	}
	public void setMaterialId(String materialId) {
		MaterialId = materialId;
	}
	public String getStockOfMaterialCount() {
		return StockOfMaterialCount;
	}
	public void setStockOfMaterialCount(String stockOfMaterialCount) {
		StockOfMaterialCount = stockOfMaterialCount;
	}
	public String getStockOfMaterialAddress() {
		return StockOfMaterialAddress;
	}
	public void setStockOfMaterialAddress(String stockOfMaterialAddress) {
		StockOfMaterialAddress = stockOfMaterialAddress;
	}
	
	
}
