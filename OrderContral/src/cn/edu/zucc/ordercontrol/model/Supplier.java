package cn.edu.zucc.ordercontrol.model;

public class Supplier {
	
	private String SupplierID;
	private String SupplierName;
	private String SupplierAddress;
	private String SupplierContacts;
	private String SupplierPhone;
	private String SupplierBriefIntroduction;
	public String getSupplierID() {
		return SupplierID;
	}
	public void setSupplierID(String supplierID) {
		SupplierID = supplierID;
	}
	public String getSupplierName() {
		return SupplierName;
	}
	public void setSupplierName(String supplierName) {
		SupplierName = supplierName;
	}
	public String getSupplierAddress() {
		return SupplierAddress;
	}
	public void setSupplierAddress(String supplierAddress) {
		SupplierAddress = supplierAddress;
	}
	public String getSupplierContacts() {
		return SupplierContacts;
	}
	public void setSupplierContacts(String supplierContacts) {
		SupplierContacts = supplierContacts;
	}
	public String getSupplierPhone() {
		return SupplierPhone;
	}
	public void setSupplierPhone(String supplierPhone) {
		SupplierPhone = supplierPhone;
	}
	public String getSupplierBriefIntroduction() {
		return SupplierBriefIntroduction;
	}
	public void setSupplierBriefIntroduction(String supplierBriefIntroduction) {
		SupplierBriefIntroduction = supplierBriefIntroduction;
	}
	
}
