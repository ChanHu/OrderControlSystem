package cn.edu.zucc.ordercontrol.model;

public class Customer {
	private String CustomerID;
	private String CustomerName;
	private String CustomerAddresss;
	private String CustomerContacts;
	private String CustomerPhone;
	public String getCustomerID() {
		return CustomerID;
	}
	public void setCustomerID(String customerID) {
		CustomerID = customerID;
	}
	public String getCustomerName() {
		return CustomerName;
	}
	public void setCustomerName(String customerName) {
		CustomerName = customerName;
	}
	public String getCustomerAddresss() {
		return CustomerAddresss;
	}
	public void setCustomerAddresss(String customerAddresss) {
		CustomerAddresss = customerAddresss;
	}
	public String getCustomerContacts() {
		return CustomerContacts;
	}
	public void setCustomerContacts(String customerContacts) {
		CustomerContacts = customerContacts;
	}
	public String getCustomerPhone() {
		return CustomerPhone;
	}
	public void setCustomerPhone(String customerPhone) {
		CustomerPhone = customerPhone;
	}
	
}
